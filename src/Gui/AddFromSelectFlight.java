    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        String passNum = this.PassNumber.getText();
        String Fname = this.FirsName.getText();
        String Lname = this.LastName.getText();
        Date datum = this.jDateChooser1.getDate();
        String country = (String) this.CountyBox.getSelectedItem();
        LocalDate date = datum.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Customer klant = new Customer(passNum, Fname, Lname, country, date);

        if (passNum.equals("") || Fname.equals("") || Lname.equals("") || datum.equals("") || country.equals("")) {

            JOptionPane.showMessageDialog(null, "Please fill in all fields ");
        } else {
            try {
                DBCustomer.save(klant);
                JOptionPane.showMessageDialog(null, klant.getFirstName() + " " + klant.getLastName() + " has been added!\n");
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Are you ready? ", "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(null, "Now Select the Route");
                    SelectFlightFrame j = new SelectFlightFrame(this.route, this.vetrekDatum, numberOfCustomers);
                    j.setVisible(true);

                    this.hide();
                } else {
                    JOptionPane.showMessageDialog(null, "Add next Customer");
                    AddFromSelectFlight j = new AddFromSelectFlight(this.route, this.vetrekDatum, numberOfCustomers);
                    j.setVisible(true);
                    this.hide();

                }
            } catch (DBException ex) {
                java.util.logging.Logger.getLogger(AddFromSelectFlight.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);

            }
        }
    }//GEN-LAST:event_AddButtonActionPerformed

